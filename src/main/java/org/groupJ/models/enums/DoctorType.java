package org.groupJ.models.enums;
/**
 * Enumerates the types of medical practices that doctors can specialize in within the healthcare system.
 * This enum is used to categorize the type of appointments patients request, allowing doctors to efficiently filter
 * and view appointments that match their specialization.
 * It is also used to differentiate the rates for each category in billing
 */
public enum DoctorType {
    GENERAL_PRACTICE,
    SPECIALIST
}